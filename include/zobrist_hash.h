  constexpr Zobrist_hash& operator=(Zobrist_hash&& other) = default;

  constexpr auto operator<=>(Zobrist_hash const& other) const = default;

  // These functions are their own inverses. To keep the hash up to date, they
  // should be called with both the old and new values of a change in board
  // state, to unset the old value and then set the new value.
  constexpr void update_en_passant_square(Bitboard ep_square)
  {
    if (!ep_square.is_empty())
    {
      m_hash ^= m_random_numbers[c_en_passant_file_offset + Coordinates{ep_square.bitscan_forward()}.y()];
    }
  }

  constexpr void update_castling_rights(Castling_rights rights)
  {
    // Castling rights is a uint8 with 16 possible values, so we can use it as
    // an array offset
    m_hash ^= m_random_numbers[c_castling_rights_offset + static_cast<uint8_t>(rights)];
  }

  constexpr void update_piece_location(Color color, Piece piece, Coordinates location)
  {
    // This array can be indexed by color
    constexpr std::array piece_offsets{c_black_pieces_offset, c_white_pieces_offset};

    auto const piece_offset = static_cast<uint8_t>(piece) - static_cast<uint8_t>(Piece::pawn);
    auto const rand_index_base =
      piece_offsets[static_cast<uint8_t>(color)] + (c_board_dimension_squared * piece_offset);
    m_hash ^= m_random_numbers[rand_index_base + location.square_index()];
  }

  // Should be called once when the player to move changes
  constexpr void update_player_to_move()
  {
    m_hash ^= m_random_numbers[c_black_to_move_offset];
  }

  constexpr zhash_t get_hash() const
  {
    return m_hash;
  }

  constexpr operator zhash_t() const
  {
    return m_hash;
  }

